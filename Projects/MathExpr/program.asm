; This program solves the ((13+44)*2-32)/9 expression

SEG1 SEGMENT
	OPER1 DB 13
	OPER2 DB 44
	OPER3 DB 2
	OPER4 DB 32
	OPER5 DB 9
	RESULT DW ?
SEG1 ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:SEG1
START:
	MOV AX,SEG1
	MOV DS,AX

	MOV AH,OPER1	; Addition
	ADD AH,OPER2

	MOV AL,AH		; Multiplication
	MOV BL,OPER3
	MUL BL

	SUB AL,OPER4	; Substraction

	MOV BH,OPER5	; Division
	DIV BH

	MOV RESULT,AX

	MOV AH,4CH
	INT 21H
CODE ENDS
END START
